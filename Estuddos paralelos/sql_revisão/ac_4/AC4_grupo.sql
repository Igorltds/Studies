CREATE VIEW VIEW_CATEGORIA_PRODUTO AS
SELECT
VENDA.CATEGORIA_PRODUTO.NOME AS CATEGORIA_PRODUTO,
VENDA.PRODUTO.CUSTO_PADRAO,
UPPER(TRIM(CONCAT(VENDA.PRODUTO.NOME, ' - ', VENDA.PRODUTO.COR))) NOME_PRODUTO_COM_COR,
CASE WHEN VENDA.PRODUTO.TAMANHO = 'P' THEN '32'
	 WHEN VENDA.PRODUTO.TAMANHO = 'M' THEN '49'
	 WHEN VENDA.PRODUTO.TAMANHO = 'L' THEN '52'
	 WHEN VENDA.PRODUTO.TAMANHO = 'XL' THEN '70' 
	 ELSE ISNULL(VENDA.PRODUTO.TAMANHO,'0') END TAMANHO_ATUALIZADO,
LEFT(VENDA.PRODUTO.NUMERO_PRODUTO, 2) CODIGO_PRODUTO,
RIGHT(VENDA.PRODUTO.NUMERO_PRODUTO, 2) CODIGO_FINAL,
REPLACE(VENDA.MODELO_PRODUTO.NOME,'-',' ') NOME_COM_ESPAÇO,
RANK() OVER (ORDER BY VENDA.PRODUTO.CUSTO_PADRAO desc) MAIS_CAROS 
FROM VENDA.CATEGORIA_PRODUTO, VENDA.PRODUTO, VENDA.MODELO_PRODUTO

SELECT * FROM VIEW_CATEGORIA_PRODUTO


