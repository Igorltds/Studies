CREATE VIEW VENDA_PRODUTO AS
SELECT 
VENDA.PRODUTO.NOME,
SUM(VENDA.DETALHE_PEDIDO.QUANTIDADE_PEDIDO) SOMA,
COUNT (DISTINCT VENDA.DETALHE_PEDIDO.QUANTIDADE_PEDIDO) CONTAGEM,
VENDA.PRODUTO.DATA_TERMINO_VENDA AS DATA_ULTIMA_COMPRA,
VENDA.PRODUTO.DATA_INICIO_VENDA AS DATA_PRIMEIRA_COMPRA
FROM VENDA.PRODUTO, VENDA.DETALHE_PEDIDO
WHERE VENDA.PRODUTO.DATA_TERMINO_VENDA IS NOT NULL
GROUP BY VENDA.PRODUTO.NOME, VENDA.PRODUTO.DATA_TERMINO_VENDA, VENDA.PRODUTO.DATA_INICIO_VENDA
--ORDER BY DATA_ULTIMA_COMPRA DESC, DATA_PRIMEIRA_COMPRA ASC

SELECT * FROM VENDA_PRODUTO


